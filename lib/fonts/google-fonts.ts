export type FontCategory = keyof typeof fontLibrary
export type FontEntry = typeof fontLibrary[FontCategory][number]

// Get all fonts flattened
export const getAllFonts = (): FontEntry[] => {
  return Object.values(fontLibrary).flat()
}

// Get fonts by category
export const getFontsByCategory = (category: FontCategory): FontEntry[] => {
  return fontLibrary[category] || []
}

// Get font by name
export const getFontByName = (name: string): FontEntry | undefined => {
  return getAllFonts().find(font => font.name === name)
}

// Get available weights for a font
export const getFontWeights = (fontName: string): number[] => {
  const font = getFontByName(fontName)
  return font?.weights || []
}

// Check if a font is variable
export const isVariableFont = (fontName: string): boolean => {
  const font = getFontByName(fontName)
  return font?.variable || false
}

// Get font URL
export const getFontUrl = (fontName: string): string | undefined => {
  const font = getFontByName(fontName)
  return font?.url
}

